fun main(){
    val booleanVar: Boolean = true // Boolean --> use to store true of false value
    val byteVar: Byte = 127 // Byte --> use to store byte value
    val shortVar: Short = 32767   // Short --> use to store short
    val intVar: Int = 2147483647   //Int --> use to store integer value
    val longVar: Long = 9223372036854775807L  //Long --> use to store long integer value
    val floatVar: Float = 3.14f     //Float --> use to store decimal integer value
    val doubleVar: Double = 3.14159265358979323846    //Double --> use to store double decimal integer value
    val charVar: Char = 'A'   // Char --> use to store char value
    val stringVar: String = "Hello, world!"    // String --> use to store string value

    println(booleanVar)
    println(byteVar)
    println(shortVar)
    println(intVar)
    println(longVar)
    println(floatVar)
    println(doubleVar)
    println(charVar)
    println(stringVar)
}
